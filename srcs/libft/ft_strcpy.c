#include "includes/libft.h"

char	*ft_strcpy(char *cpy, const char *src)
{
	int	i;

	i = 0;
	while (src[i])
	{
		cpy[i] = src[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
